def to_excel(df):
    output = io.BytesIO()
    # ‡πÉ‡∏ä‡πâ engine='xlsxwriter' ‡πÅ‡∏•‡∏∞ encoding ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏†‡∏≤‡∏©‡∏≤‡πÑ‡∏ó‡∏¢
    writer = pd.ExcelWriter(output, engine='xlsxwriter')
    df.to_excel(writer, index=False, sheet_name='Summary')
    writer.close()
    processed_data = output.getvalue()
    return processed_data

